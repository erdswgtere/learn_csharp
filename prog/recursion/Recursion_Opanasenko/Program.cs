** F(n) = F(n–1) + n – 2, при n > 15, кратных 3               **
** F(n) = F(n–2) + n + 2, при n > 15, не кратных 3            **
** Определите количество натуральных значений n из отрезка    **
** [1; 1000], для которых все цифрызначения F(n) чётные.      **
